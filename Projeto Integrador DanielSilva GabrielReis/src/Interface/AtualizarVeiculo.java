    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (this.veiculo !=null){
            VeiculoRN veiculoRN = new VeiculoRN();
            veiculo.setStatus(this.status.getSelectedItem().toString());
            if (!veiculoRN.atualizarVeiculo(this.veiculo).equals("atualizou")){
                JOptionPane.showMessageDialog(null,"Erro ao atualizar: ");
                return;
            }
        }else{
            JOptionPane.showMessageDialog(null,"Veiculo nao encontrado");
            return;
        }
        JOptionPane.showMessageDialog(null,"Atualizado com sucesso ");
        TelaInicial telaInicial = new TelaInicial(this.funcionario);
        telaInicial.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

