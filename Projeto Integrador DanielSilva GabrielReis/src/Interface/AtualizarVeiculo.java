    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String placa = tFPlaca.getText();
        this.veiculo = new Veiculo();
        VeiculoRN veiculoRN = new VeiculoRN();
        veiculo = veiculoRN.buscarVeiculo(placa);
        if (veiculo == null){
            JOptionPane.showMessageDialog(null,"Veiculo nao cadastrado");
                return;
        }
        this.CTFModelo.setText(veiculo.getModelo());
        this.CTFAno.setText(Integer.toString(veiculo.getAno()));
        this.CTFChassi.setText(veiculo.getChassi());
        this.CTFCor.setText(veiculo.getCor());
        BigDecimal bd = veiculo.getKilometragem();
        this.CTFKm.setText(bd.toString());
        this.status.setSelectedItem(this.veiculo.getStatus());
        this.jCCombustivel.setSelectedItem(veiculo.getCombustivel());            
        this.CTFCor.setText(veiculo.getCor());
    }//GEN-LAST:event_jButton1ActionPerformed

