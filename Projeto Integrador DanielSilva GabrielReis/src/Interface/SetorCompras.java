    private void jBConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConfirmarActionPerformed

        Veiculo veiculo = new Veiculo();
        veiculo.setModelo(this.tFModelo.getText());
        veiculo.setCor(this.tFCor.getText());
        veiculo.setPlaca(this.tFPlaca.getText());
        veiculo.setChassi(this.tFChassi.getText());
        
        try{
            veiculo.setAno(Integer.parseInt(this.tFAno.getText()));
            veiculo.setKilometragem(new BigDecimal(this.tFKm.getText()));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Preencha os campos Corretamente");
            return;
        }
        veiculo.setCombustivel(jCCombustivel.getSelectedItem().toString());
        
        VeiculoRN veiculoRN = new VeiculoRN();
        String mensagem = veiculoRN.cadastrarVeiculo(veiculo);//aqui nao cadastra ainda s√≥ valida os dados
        if (mensagem == null){
            JOptionPane.showMessageDialog(null,"Preencha os campos corretamente");
            return;
        }
        
        //buscar o tipo veiculo para inserir em veiculo
        
        Tipoveiculo tipoveiculo = new Tipoveiculo();
        tipoveiculo.setTipo(jCTipoVeiculo.getSelectedItem().toString());
        
        
       veiculo.setTipoveiculo(tipoveiculo);
       
       
       
       
       
        //--compra
        Compra compra = new Compra(); 
        Date dataCompra=null;
        try{    //Formato data do VO √© do tipo Date 
           dataCompra = new SimpleDateFormat("dd/MM/yyyy").parse(tfDataCompra.getText()); 
           compra.setDatacompra(dataCompra);
        }catch(Exception erro){
            JOptionPane.showMessageDialog(null,"Erro na data: "+erro.getMessage());
            return;
        }
                  
                   
        CadastrarFornecedor cadastrarFornecedor = new CadastrarFornecedor(this.funcionario, veiculo, compra);
        cadastrarFornecedor.setVisible(true);
        this.setVisible(false);
   
        
    }//GEN-LAST:event_jBConfirmarActionPerformed

