    private void botaoConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoConfirmarActionPerformed
        String mensagem=null;    
        if (this.endereco == null){//se entrar endereco nao existe
                this.endereco = new Endereco(); 
                endereco.setRua(rua.getText());
                endereco.setBairro(bairro.getText());
                endereco.setComplemento(complemento.getText());
                endereco.setCidade(cidade.getText());
                endereco.setUf(uf.getText());
                endereco.setCep(cep.getText());

                EnderecoRN enderecoRN = new EnderecoRN();
                mensagem = enderecoRN.cadastrarEndereco(endereco);
                if (!mensagem.equals("CADASTROU")){
                    JOptionPane.showMessageDialog(null,mensagem);
                    return;
                }else{//bscar fk de endereco
                    
                }      
        } 
        
       try{  //erro na conversao de numero
                if(cliente!=null){
                    cliente.setNumendereco(Integer.parseInt(numero.getText()));
                    cliente.setEndereco(this.endereco);
                    ClienteRN clienteRN = new ClienteRN();
                    mensagem = clienteRN.cadastrarCliente(cliente);
                    JOptionPane.showMessageDialog(null,mensagem);
                }
                else if(funcionario!=null){
                    funcionario.setNumendereco(Integer.parseInt(numero.getText()));
                    funcionario.setEndereco(this.endereco);
                    FuncionarioRN funcionarioRN = new FuncionarioRN();
                    mensagem = funcionarioRN.cadastrarFuncionario(funcionario);
                    JOptionPane.showMessageDialog(null,mensagem);
                }
                else{
                    fornecedor.setNumendereco(Integer.parseInt(numero.getText()));
                    fornecedor.setEndereco(this.endereco);             
                    FornecedorRN fornecedorRN = new FornecedorRN();
                    mensagem = fornecedorRN.cadastrarFornecedor(fornecedor);
                    JOptionPane.showMessageDialog(null,mensagem);
                }
        }catch(NumberFormatException e){
               JOptionPane.showMessageDialog(null,"Erro: "+e.getMessage());    
        }
            
    }//GEN-LAST:event_botaoConfirmarActionPerformed

    private void ruaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_ruaFocusLost
        this.endereco = new Endereco();
        EnderecoRN enderecoRN = new EnderecoRN();
        this.endereco = enderecoRN.buscarEndereco(this.cidade.getText().toUpperCase(), this.rua.getText().toUpperCase());
        
        if(this.endereco != null){
            this.bairro.setText(this.endereco.getBairro());
            this.bairro.setEditable(false);
            this.cep.setText(this.endereco.getCep());
            this.cep.setEditable(false);
            this.complemento.setText(this.endereco.getComplemento());
            this.complemento.setEditable(false);
            this.uf.setText(this.endereco.getUf());
            this.uf.setEditable(false);            
        }else{
            this.bairro.setEditable(true);
            this.cep.setEditable(true);
            this.complemento.setEditable(true);
            this.uf.setEditable(true); 
            this.endereco=null;
        }
    }//GEN-LAST:event_ruaFocusLost

