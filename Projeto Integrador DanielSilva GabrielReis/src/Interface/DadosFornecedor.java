    private void DFBTConfirmar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DFBTConfirmar1ActionPerformed
            Endereco endereco = new Endereco(); 
            Fornecedor fornecedor = new Fornecedor();
            endereco.setRua(rua.getText());
            endereco.setBairro(complemento.getText());
            endereco.setComplemento(complemento.getText());
            endereco.setCidade(cidade.getText());
            endereco.setUf(uf.getText());
            endereco.setCep(cep.getText());

            EnderecoRN enderecoRN = new EnderecoRN();
            String mensagem = enderecoRN.atualizarEndereco(endereco);
            fornecedor.setNome(nome.getText());
            fornecedor.setTelefone(telefone.getText());
                   
            fornecedor.setNumendereco(Integer.parseInt(numero.getText()));
            fornecedor.setEndereco(endereco);             
            FornecedorRN fornecedorRN = new FornecedorRN();
            mensagem = fornecedorRN.atualizarFornecedor(fornecedor);
            JOptionPane.showMessageDialog(null,mensagem);
    }//GEN-LAST:event_DFBTConfirmar1ActionPerformed

