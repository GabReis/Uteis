    private void jBConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConfirmarActionPerformed
        float kmAntiga;
        
        Date dataEntrega=null;
        try{    //Formato data do VO é do tipo Date 
           dataEntrega = new SimpleDateFormat("dd/MM/yyyy").parse(tFDataEntrega.getText()); 
        }catch(Exception erro){
            JOptionPane.showMessageDialog(null,"Erro na data: "+erro.getMessage());
            return;
        }
            String mensagem="";
            VeiculoRN veiculoRN= new VeiculoRN();
 
           Aluguel aluguel = new Aluguel();
           AluguelRN aluguelRN = new AluguelRN();
           aluguel = aluguelRN.buscarUmAluguelSemDataDevolucao(this.veiculo);
           
           if (aluguel!=null){//se encontrou aluguel entao será redireciondo para o setor de cobrança passando  o aluguel como parametro para o contrutor
               kmAntiga = this.veiculo.getKilometragem().floatValue();//para usar no calculo do aluguel
               this.veiculo.setStatus("DISPONIVEL");
               mensagem = veiculoRN.atualizarVeiculo(this.veiculo, this.tFKmAtual.getText());
               
                if (mensagem != "atualizou"){
                    JOptionPane.showMessageDialog(null,""+mensagem);
                    return;
                }
                
                        
               
                //adiciona data de entrega no aluguel
               mensagem = aluguelRN.atualizarDataDevolucao(dataEntrega, aluguel); 
                
                if (mensagem != "OK"){//erro ao atualizar  
                    JOptionPane.showMessageDialog(null,""+mensagem);
                    return;
                }
                //JOptionPane.showMessageDialog(null,"data Devolucao: "+aluguel.getDatadevolucao());
                
               
           }else{
                JOptionPane.showMessageDialog(null,"Aluguel nao encontrado");
                return;
           }
           
           
        
         SetorCobranca setorCobranca = new SetorCobranca(aluguel, this.funcionario, kmAntiga);
         setorCobranca.setVisible(true);
         this.setVisible(false);//oculta essa tela "setor de entrada"
        
        //JOptionPane.showMessageDialog(null,"CodAluguel: "+aluguel.getCodaluguel());
        
    }//GEN-LAST:event_jBConfirmarActionPerformed

