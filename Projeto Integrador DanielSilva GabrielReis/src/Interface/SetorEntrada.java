    private void botaoOKbuscarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoOKbuscarCarroActionPerformed
       //ATENCAO setar o atributo veiculo dessa classe com o veiculo buscado no banco
        //porque ele é enviado p/ o setor de manuteção.
        //this.veiculo = veiculo; //sendo que this.veiculo é o atributo dessa tela, e veiculo é veiculo retornado do banco quando vc colocar pra buscar pela placa
        this.tFDataEntrega.setEditable(false);
        this.tFKmAtual.setEditable(false);
        this.tFMostrarDadosVeiculo.setEditable(false);
        this.jBConfirmar.setEnabled(false);
        this.jBAdicionarmanutencao.setEnabled(false);
        String placa = tFPlaca.getText();
        VeiculoRN veiculoRN = new VeiculoRN();
        this.veiculo = veiculoRN.buscarVeiculo(placa);
        
        if (this.veiculo == null){
            this.tFMostrarDadosVeiculo.setText("Veículo não encontrado");           
        }else{           
            String mostrarVeiculo = 
                    "Placa: "+this.veiculo.getPlaca()+"\nStatus: "+this.veiculo.getStatus()+
                    "\nChassi: "+this.veiculo.getChassi()+"\nCombustivel: "+this.veiculo.getCombustivel()+"\nCor: "+this.veiculo.getCor()+
                    "\nModelo: "+this.veiculo.getModelo()+"\nAno: "+this.veiculo.getAno()+
                    "\nKilometragem: "+this.veiculo.getKilometragem();
            tFMostrarDadosVeiculo.setText(mostrarVeiculo);                    
                    
            this.tFDataEntrega.setEditable(true);
            this.tFKmAtual.setEditable(true);
            this.jBConfirmar.setEnabled(true);
            this.jBAdicionarmanutencao.setEnabled(true);  
        }
    }//GEN-LAST:event_botaoOKbuscarCarroActionPerformed

